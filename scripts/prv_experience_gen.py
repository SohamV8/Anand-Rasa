"""Experience-first patron review bodies — no product title repetition."""

import re
from prv_category_rules import BANNED_BODY_PATTERNS, FORBIDDEN


def _cap(s: str) -> str:
    s = s.strip()
    if not s:
        return s
    return s[0].upper() + s[1:]


def _validate(axis: str, body: str) -> bool:
    low = body.lower()
    for pat in BANNED_BODY_PATTERNS:
        if re.search(pat, body, re.IGNORECASE):
            return False
    for term in FORBIDDEN.get(axis, []):
        if term.lower() in low:
            return False
    if re.search(r"\b(long lasting|pure sandalwood|natural ingredients|premium texture)\b", low):
        return False
    return True


def _opening_key(opening: str) -> str:
    return opening.strip().lower()


def build_reviews(axis: str, openings: list[str], experiences: list[str], closers: list[str] | None = None, count: int = 95) -> list[str]:
    closers = closers or []
    out: list[str] = []
    used: set[str] = set()
    o_n, e_n = len(openings), len(experiences)
    c_n = len(closers) or 1

    for i in range(count):
        found = False
        for attempt in range(o_n * e_n * max(c_n, 1) * 8):
            o = openings[(i * 17 + attempt * 13) % o_n]
            e = experiences[(i * 11 + attempt * 7) % e_n]
            if closers and (i + attempt) % 3 != 0:
                c = closers[(i * 13 + attempt * 3) % c_n]
                body = _cap(f"{o} {e} {c}")
            else:
                body = _cap(f"{o} {e}")
            if body in used:
                continue
            if not _validate(axis, body):
                continue
            used.add(body)
            out.append(body)
            found = True
            break
        if not found:
            raise RuntimeError(f"Could not build review {i} for {axis} (openings={o_n}, experiences={e_n})")
    return out[:count]


def combo_openings(prefixes: list[str], suffixes: list[str], min_count: int = 96) -> list[str]:
    out: list[str] = []
    for i in range(min_count * 3):
        p = prefixes[i % len(prefixes)]
        s = suffixes[(i * 7 + 3) % len(suffixes)]
        text = f"{p} {s}".strip()
        if text not in out:
            out.append(text)
        if len(out) >= min_count:
            return out
    return out


