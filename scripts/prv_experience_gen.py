# Shorter pools for secondary axes — still experience-first

def _secondary(axis: str, hooks: list[str], experiences: list[str]) -> list[str]:
    variants: list[str] = []
    lead_ins = [
        "", "Honestly — ", "Unexpectedly — ", "Pehli baar — ", "Repeat buy — ",
        "4 star note — ", "Second bottle — ", "Festival week — ", "Gift story — ",
        "After two weeks — ", "My mother — ", "My wife — ", "My father — ",
        "We — ", "Bought this — ", "Initially unsure — ", "Didn't expect — ",
        "Tried during — ", "Been using — ", "Kept it — ",
    ]
    for h in hooks:
        for lead in lead_ins:
            text = f"{lead}{h}".strip()
            if text not in variants:
                variants.append(text)
            if len(variants) >= 96:
                break
        if len(variants) >= 96:
            break
    openings = variants[:96]
    return build_reviews(axis, openings, experiences, count=95)


