from prv_category_rules import SHORT

S = SHORT


def _compose(parts: list[str]) -> list[str]:
    out = []
    for p in parts:
        p = p.strip()
        if not p:
            continue
        if S not in p:
            p = f"{S} — {p[0].lower()}{p[1:]}" if p else p
        out.append(p)
    return out


