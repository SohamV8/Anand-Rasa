def _gen(axis: str, hooks: list[str], mids: list[str], ends: list[str], count: int = 90) -> list[str]:
    """Combine unique triples for category-appropriate reviews."""
    out = []
    used = set()
    hi, mi, ei = len(hooks), len(mids), len(ends)
    for i in range(count):
        for attempt in range(hi * mi * ei):
            j = (i + attempt) % hi
            k = (i * 3 + attempt) % mi
            l = (i * 7 + attempt) % ei
            text = f"{hooks[j]} {S} {mids[k]} {ends[l]}".replace("  ", " ").strip()
            if S not in text:
                text = f"{S} — {text[0].lower()}{text[1:]}" if text else text
            if text not in used:
                used.add(text)
                out.append(text)
                break
        else:
            out.append(f"{hooks[i % hi]} {S} {mids[i % mi]} {ends[i % ei]} ({i}).")
    return out[:count]


